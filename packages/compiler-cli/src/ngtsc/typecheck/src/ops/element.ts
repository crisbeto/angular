import {TmplAstElement} from '@angular/compiler';
import {TcbNode, TcbOp} from './base';
import type {Context} from './context';
import type {Scope} from './scope';

/**
 * A `TcbOp` which creates an expression for a native DOM element (or web component) from a
 * `TmplAstElement`.
 *
 * Executing this operation returns a reference to the element variable.
 */
export class TcbElementOp extends TcbOp {
  constructor(
    private tcb: Context,
    private scope: Scope,
    private element: TmplAstElement,
  ) {
    super();
  }

  override get optional() {
    // The statement generated by this operation is only used for type-inference of the DOM
    // element's type and won't report diagnostics by itself, so the operation is marked as optional
    // to avoid generating statements for DOM elements that are never referenced.
    return true;
  }

  override execute(): TcbNode {
    const id = this.tcb.allocateId();
    const idNode = new TcbNode(id);
    // Element doesn't have nameSpan? startSourceSpan covers the tag name `<div`.
    idNode.addParseSpanInfo(this.element.startSourceSpan || this.element.sourceSpan);

    // Add the declaration of the element using document.createElement.
    const initializer = new TcbNode(`document.createElement("${this.element.name}")`);
    initializer.addParseSpanInfo(this.element.startSourceSpan || this.element.sourceSpan);
    const stmt = new TcbNode(`var ${idNode.print()} = ${initializer.print()}`);
    stmt.addParseSpanInfo(this.element.startSourceSpan || this.element.sourceSpan);
    this.scope.addStatement(stmt);
    return idNode;
  }
}
